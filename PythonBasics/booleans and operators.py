'''
Comparison operators
== equal
!= not equal
> greater than
< less than
>= greater than or equal to
<= less than or equal to
'''
print(2 == 3)
print(2 != 3)
print(2 > 3)
print(2 >= 3)
print(2 <= 3)
print(2 < 3)

'''
Logical operators
and
or
not
'''
print(2 == 3 and 2 < 3)
print(2 == 3 or 2 <= 3)
print(not(2 == 3))

